module TopModule (
    input  logic [3:0] x,
    output logic       f
);

    // The K-map mapping based on the specification:
    // x[4] is x[0], x[3] is x[1], x[2] is x[2], x[1] is x[3]
    // Row index (x[3]x[4]) -> (x[1]x[0])
    // Col index (x[1]x[2]) -> (x[3]x[2])
    // 
    // Let's derive the truth table for x[3:0]:
    // x[3]x[2]x[1]x[0] | Row (x[3]x[0]) | Col (x[1]x[2]) | K-map value
    // ----------------|----------------|----------------|------------
    // 0000             | 00             | 00             | d
    // 0001             | 01             | 00             | 0
    // 0010             | 00             | 10             | d
    // 0011             | 01             | 10             | 0
    // 0100             | 00             | 01             | 0
    // 0101             | 01             | 01             | d
    // 0110             | 00             | 11             | d
    // 0111             | 01             | 11             | 1
    // 1000             | 10             | 00             | 1
    // 1001             | 11             | 00             | 1
    // 1010             | 10             | 10             | d
    // 1011             | 11             | 10             | d
    // 1100             | 10             | 01             | 1
    // 1101             | 11             | 01             | 1
    // 1110             | 10             | 11             | 0
    // 1111             | 11             | 11             | d

    always @(*) begin
        case (x) 
            4'b0000: f = 1'b0; // d
            4'b0001: f = 1'b0; // 0
            4'b0010: f = 1'b0; // d
            4'b0011: f = 1'b0; // 0
            4'b0100: f = 1'b0; // 0
            4'b0101: f = 1'b0; // d
            4'b0110: f = 1'b0; // d
            4'b0111: f = 1'b1; // 1
            4'b1000: f = 1'b1; // 1
            4'b1001: f = 1'b1; // 1
            4'b1010: f = 1'b1; // d
            4'b1011: f = 1'b1; // d
            4'b1100: f = 1'b1; // 1
            4'b1101: f = 1'b1; // 1
            4'b1110: f = 1'b0; // 0
            4'b1111: f = 1'b1; // d
            default:  f = 1'b0;
        endcase
    end

endmodule